package org.deletethis.hardcode;

public class CrossRootReferenceException extends HardcodeException {
    public CrossRootReferenceException() {
    }

    public CrossRootReferenceException(String message) {
        super(message);
    }

    public CrossRootReferenceException(String message, Throwable cause) {
        super(message, cause);
    }

    public CrossRootReferenceException(Throwable cause) {
        super(cause);
    }
}
